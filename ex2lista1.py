a = float(input("Escreva um número1: "))
aa = float(input("Escreva o peso do número1"))
b = float(input("Escreva um número2: "))
bb = float(input("Escreva o peso do número2"))
c = float(input("Escreva um número3: "))
cc = float(input("Escreva o peso do número3"))

media = ((a*aa) + (b*bb) + (c*cc))/ (aa + bb + cc)

print(" A média ponderada é de:", media)

