# Exemplos variados de uso da função range de
# acordo com a variação de seus parâmetros.

for e in range(1,4,1):
    print(e, ' ', end="")
print()

for e in range(10,0,-1):
    print(e, ' ', end="")
print()

for e in range(8):
    print(e, ' ', end="")
print()

for e in range(0,101,10):
    print(e, ' ', end="")
print()